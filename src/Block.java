import java.awt.*;

public class Block {
    double sx;
    double sy;
    double ssize;
    double svelocityX;
    double svelocityY;

    Color scolor;

    public Block(double x, double y, double velocityX, double velocityY, Color color) {
        this.sx = sx;
        this.sy = sy;
        this.ssize = ssize;
        this.svelocityX = svelocityX;
        this.svelocityY = svelocityY;
        this.scolor = scolor;
    }
}
